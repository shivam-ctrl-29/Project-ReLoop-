// ── PDF: Monthly Impact Report ───────────────────────────────────────────────
function downloadImpactPDF(monthly: any[], totals: any) {
  const pdf = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });
  const W = 210;
  const generated = new Date().toLocaleDateString('en-IN', { day: 'numeric', month: 'long', year: 'numeric' });

  pdf.setFillColor(27, 94, 32); pdf.rect(0, 0, W, 55, 'F');
  pdf.setFillColor(245, 158, 11); pdf.rect(0, 55, W, 2.5, 'F');

  pdf.setTextColor(255,255,255); pdf.setFontSize(20); pdf.setFont('helvetica','bold');
  pdf.text('ReLoop', W/2, 18, { align: 'center' });
  pdf.setFontSize(8); pdf.setFont('helvetica','normal'); pdf.setTextColor(180,220,180);
  pdf.text('CIRCULAR RESOURCE MANAGEMENT PLATFORM', W/2, 25, { align: 'center' });
  pdf.setFontSize(15); pdf.setFont('helvetica','bold'); pdf.setTextColor(255,255,255);
  pdf.text('Monthly Impact Report — 2026', W/2, 40, { align: 'center' });
  pdf.setFontSize(8); pdf.setFont('helvetica','normal'); pdf.setTextColor(200,230,200);
  pdf.text('Symbiosis University of Applied Sciences, Indore', W/2, 48, { align: 'center' });

  // Summary boxes
  const summaries = [
    ['CO2 Saved (YTD)',     `${Number(totals.total_co2||0).toFixed(1)} T`],
    ['Water Conserved',    `${(Number(totals.total_water||0)/1000).toFixed(1)} KL`],
    ['Total Revenue (YTD)',`₹${Number(totals.total_revenue||0).toLocaleString('en-IN')}`],
    ['Items Diverted',     `${Number(totals.total_items||0).toFixed(0)}`],
  ];
  const bW = 40, bX = 15, bY = 65;
  summaries.forEach(([label, val], i) => {
    const x = bX + i * (bW + 5);
    pdf.setFillColor(241, 248, 240); pdf.roundedRect(x, bY, bW, 20, 2, 2, 'F');
    pdf.setFontSize(7); pdf.setFont('helvetica','bold'); pdf.setTextColor(156,163,175);
    pdf.text(label.toUpperCase(), x+3, bY+6);
    pdf.setFontSize(11); pdf.setFont('helvetica','bold'); pdf.setTextColor(27,94,32);
    pdf.text(val, x+3, bY+15);
  });

  // Monthly table
  pdf.setFontSize(11); pdf.setFont('helvetica','bold'); pdf.setTextColor(31,42,36);
  pdf.text('Monthly Breakdown', 20, 100);
  pdf.setFillColor(27,94,32); pdf.rect(20, 103, 170, 8, 'F');
  ['Month','CO2 (T)','Water (KL)','Revenue (₹)','Oil (L)','E-Waste Items'].forEach((h, i) => {
    pdf.setFontSize(7); pdf.setFont('helvetica','bold'); pdf.setTextColor(255,255,255);
    pdf.text(h, 22 + i*28, 108);
  });

  monthly.forEach((m: any, i: number) => {
    const y = 114 + i * 8;
    if (i % 2 === 0) { pdf.setFillColor(245,247,245); pdf.rect(20, y-4, 170, 8, 'F'); }
    const row = [
      m.month,
      Number(m.co2_saved||0).toFixed(2),
      (Number(m.water_saved||0)/1000).toFixed(1),
      `${Number(m.revenue||0).toLocaleString('en-IN')}`,
      Number(m.oil_collected||0).toFixed(0),
      String(m.ewaste_items||0),
    ];
    row.forEach((val, j) => {
      pdf.setFontSize(8); pdf.setFont('helvetica','normal'); pdf.setTextColor(31,42,36);
      pdf.text(String(val), 22 + j*28, y+1);
    });
  });

  const footY = 114 + monthly.length * 8 + 10;
  pdf.setDrawColor(229,231,235); pdf.setLineWidth(0.5); pdf.line(20, footY, W-20, footY);
  pdf.setFontSize(7); pdf.setTextColor(156,163,175);
  pdf.text(`Generated by ReLoop on ${generated} · Team EcoNova · Green Tech Hackathon 2026`, W/2, footY+6, { align: 'center' });

  pdf.save('ReLoop-Monthly-Impact-Report.pdf');
}

// ── PDF: CSR/ESG Report ──────────────────────────────────────────────────────
function downloadESGPDF(totals: any) {
  const pdf = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });
  const W = 210;
  const generated = new Date().toLocaleDateString('en-IN', { day: 'numeric', month: 'long', year: 'numeric' });

  pdf.setFillColor(33, 150, 243); pdf.rect(0, 0, W, 55, 'F');
  pdf.setFillColor(245, 158, 11); pdf.rect(0, 55, W, 2.5, 'F');

  pdf.setTextColor(255,255,255); pdf.setFontSize(20); pdf.setFont('helvetica','bold');
  pdf.text('ReLoop', W/2, 18, { align: 'center' });
  pdf.setFontSize(8); pdf.setFont('helvetica','normal'); pdf.setTextColor(180,210,255);
  pdf.text('CIRCULAR RESOURCE MANAGEMENT PLATFORM', W/2, 25, { align: 'center' });
  pdf.setFontSize(15); pdf.setFont('helvetica','bold'); pdf.setTextColor(255,255,255);
  pdf.text('CSR / ESG Report — Q2 2026', W/2, 40, { align: 'center' });
  pdf.setFontSize(8); pdf.setFont('helvetica','normal'); pdf.setTextColor(200,225,255);
  pdf.text('Symbiosis University of Applied Sciences, Indore', W/2, 48, { align: 'center' });

  // SDG scores
  pdf.setFontSize(12); pdf.setFont('helvetica','bold'); pdf.setTextColor(31,42,36);
  pdf.text('SDG Alignment Scores', 20, 72);

  sdgData.forEach((s, i) => {
    const y = 80 + i * 14;
    pdf.setFillColor(241,248,240); pdf.roundedRect(20, y, 170, 11, 2, 2, 'F');
    pdf.setFontSize(9); pdf.setFont('helvetica','bold'); pdf.setTextColor(31,42,36);
    pdf.text(s.sdg, 25, y+7);
    // Progress bar
    const barW = 100, barX = 55;
    pdf.setFillColor(229,231,235); pdf.roundedRect(barX, y+3, barW, 5, 1, 1, 'F');
    const fillW = (s.score / 100) * barW;
    pdf.setFillColor(27,94,32); pdf.roundedRect(barX, y+3, fillW, 5, 1, 1, 'F');
    pdf.setFontSize(8); pdf.setFont('helvetica','bold'); pdf.setTextColor(27,94,32);
    pdf.text(`${s.score}%`, barX + barW + 4, y+7);
  });

  const impactY = 80 + sdgData.length * 14 + 15;
  pdf.setFontSize(12); pdf.setFont('helvetica','bold'); pdf.setTextColor(31,42,36);
  pdf.text('Environmental Impact Summary', 20, impactY);

  const impacts = [
    ['Carbon Footprint Reduced',  `${Number(totals.total_co2||0).toFixed(1)} tonnes CO2e`],
    ['Municipal Water Offset',    `${(Number(totals.total_water||0)/1000).toFixed(1)} KL`],
    ['Circular Economy Revenue',  `₹${Number(totals.total_revenue||0).toLocaleString('en-IN')}`],
    ['Waste Diverted from Landfill', `${Number(totals.total_items||0).toFixed(0)} items + oil`],
  ];
  impacts.forEach(([label, val], i) => {
    const y = impactY + 8 + i * 12;
    pdf.setFillColor(232,242,252); pdf.roundedRect(20, y, 170, 10, 2, 2, 'F');
    pdf.setFontSize(8); pdf.setFont('helvetica','normal'); pdf.setTextColor(91,107,99);
    pdf.text(label, 25, y+6.5);
    pdf.setFont('helvetica','bold'); pdf.setTextColor(33,150,243);
    pdf.text(String(val), W-25, y+6.5, { align: 'right' });
  });

  pdf.setFontSize(7); pdf.setTextColor(156,163,175);
  pdf.text(`Generated by ReLoop on ${generated} · Team EcoNova · Green Tech Hackathon 2026`, W/2, 285, { align: 'center' });

  pdf.save('ReLoop-CSR-ESG-Report-Q2.pdf');
}

// ── CSV: Transaction Ledger ──────────────────────────────────────────────────
function downloadLedgerCSV(transactions: any[]) {
  const header = ['Date','Category','Type','Reference','Description','Qty/Details','Amount (INR)','Status','Counterparty'];
  const rows = transactions.map((t: any) => [
    t.date,
    t.category,
    t.type,
    t.reference,
    t.description,
    t.qty,
    Number(t.amount||0).toFixed(0),
    t.status,
    t.counterparty,
  ]);
  const csv = [header, ...rows].map(r => r.map((c: any) => `"${String(c).replace(/"/g,'""')}"`).join(',')).join('\n');
  const blob = new Blob(['﻿' + csv], { type: 'text/csv;charset=utf-8;' });
  const url  = URL.createObjectURL(blob);
  const a    = document.createElement('a');
  a.href = url; a.download = 'ReLoop-Transaction-Ledger.csv'; a.click();
  URL.revokeObjectURL(url);
}

